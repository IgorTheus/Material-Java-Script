let color = "green";  //Armazena um valor usando "let", que é uma variável de bloco

switch (color) {   

    case "green": 
        console.log("Go");
        break;

    case "yellow": 
        console.log("Attention");
        break;

    case "red": 
        console.log("Stop");
        break;

    default:
        console.log("Invalid sign");

}